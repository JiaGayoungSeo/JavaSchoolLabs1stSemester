package Lab2_Whileandfor;

public class LabExercise2_Bonus2 {
    public static void main(String[] args){
        int i;
        int j;
        for(i=1;i<7;i++){
            for (j=0;j<7-i;j++){
                System.out.print(j+1);
            }
            System.out.println();
        }


    }
}
